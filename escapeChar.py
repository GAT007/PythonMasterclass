splitString = "This string has been split\nover several \nlines"
print(splitString)
tabbedString = "1\t2\t3\t4\t5"
print(tabbedString)

# Using the \ delimiter to escape characters such as ', " and \n and \t
print('The pet shop owner said "No, no \'e\'s uh, ...he\'s resting"')
print("The pet shop owner said \"No, no, 'e's uh, ...he's resting.\"")

# triple quotes
print("""The pet shop owner said "No, no, 'e's uh, ...he's resting.""")

# triple quote string with new lines

tripleQuoteSplitString = """ \
This string has \
been split \
over several \
lines""" 

print(tripleQuoteSplitString)

# Escape everything using backslash characters
# More preferred way of escaping characters
print("C:\\Users\\atallanki\\notes.txt")

# Examples for raw string
print(r"C:\Users\atallanki\notes.txt")

